class ContohTipeData12{
    public static void main(String[] args){
        char golongandarah = 'O';
        byte jarak = (byte) 130;
        short jumlahpenduduk = 1025;
        float suhu = 60.50F;
        double berat = 36.938717;
        long saldo = 3525500;
        int angkadesimal = 0x10;

        System.out.println("Golongan darah  :" + (byte) golongandarah);
        System.out.println("Jarak           :" + jarak);
        System.out.println("Jumlah penduduk :" + jumlahpenduduk);
        System.out.println("Suhu            :" + suhu);
        System.out.println("Berat           :" + berat);
        System.out.println("Saldo           :" + saldo);
        System.out.println("Angka           :" + angkadesimal);

    }
}